package Lesson_1016;

public class DataType {

    public static void main(String[] args){
        int paramsInt;
        paramsInt = 9;
        float paramsFloat = 23.0f;
        double doubleOne = 2.3,doubleTwo = 2.5;
        System.out.println(paramsInt);
        System.out.println(paramsFloat);
        System.out.println(doubleOne);
        System.out.println(doubleTwo);

        /**
         * final标志声明常量，通常使用大写字母表示常量：
         */
       final double PI = 3.14159265;
        System.out.println(PI);
    }
}
